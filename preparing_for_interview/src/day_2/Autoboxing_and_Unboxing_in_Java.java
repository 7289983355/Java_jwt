package day_2;

import java.util.ArrayList;
import java.util.List;

public class Autoboxing_and_Unboxing_in_Java {

	public static void main(String[] args) {
		
			    int a = 10;
			    int b = 20;
			    
			    List<Integer> list = new ArrayList();
			    list.add(a); //autoboxing 
			    list.add(b); // autoboxing
			    list.add(30);
	}

}


